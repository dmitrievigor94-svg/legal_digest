# GigaChat OAuth — кешируем токен в файл чтобы не превышать rate limit
# ---------------------------------------------------------------------------

GIGACHAT_AUTH_URL = "https://ngw.devices.sberbank.ru:9443/api/v2/oauth"
GIGACHAT_API_URL  = "https://gigachat.devices.sberbank.ru/api/v1/chat/completions"
GIGACHAT_MODEL    = "GigaChat"

# Файл для хранения токена между запусками скрипта
_TOKEN_CACHE_FILE = Path(os.environ.get("GIGACHAT_TOKEN_CACHE", "/tmp/gigachat_token.json"))


class _TokenManager:
    """
    Кеширует токен на диске — токен живёт 30 минут,
    повторные запуски скрипта используют тот же токен.
    Это важно: Сбер ограничивает количество запросов токена в минуту.
    """

    def get_token(self, auth_key: str) -> str:
        # Сначала проверяем кеш на диске
        cached = self._load_cache()
        if cached:
            return cached

        # Получаем новый токен
        resp = httpx.post(
            GIGACHAT_AUTH_URL,
            headers={
                "Content-Type": "application/x-www-form-urlencoded",
                "Accept": "application/json",
                "RqUID": "0deeb919-bfd8-45ed-a502-13eba6f85498",
                "Authorization": f"Basic {auth_key}",
            },
            data={"scope": "GIGACHAT_API_PERS"},
            verify=False,
            timeout=15,
        )
        resp.raise_for_status()
        data = resp.json()

        token = data["access_token"]
        # expires_at в миллисекундах
        expires_at = data.get("expires_at", 0) / 1000

        self._save_cache(token, expires_at)
        return token

    def _load_cache(self) -> Optional[str]:
        try:
            if not _TOKEN_CACHE_FILE.exists():
                return None
            cached = json.loads(_TOKEN_CACHE_FILE.read_text())
            # Проверяем что токен ещё действителен (с запасом 60 сек)
            if cached.get("expires_at", 0) > time.time() + 60:
                return cached["token"]
        except Exception:
            pass
        return None

    def _save_cache(self, token: str, expires_at: float) -> None:
        try:
            _TOKEN_CACHE_FILE.write_text(
                json.dumps({"token": token, "expires_at": expires_at})
            )
        except Exception:
            pass


_token_manager = _TokenManager()


# ---------------------------------------------------------------------------
# Rate limiter
# ---------------------------------------------------------------------------

class _RateLimiter:
    def __init__(self, max_per_minute: int = 20):
        self.interval = 60.0 / max_per_minute
        self._last_call: float = 0.0

    def wait(self) -> None:
        now = time.monotonic()
        elapsed = now - self._last_call
        if elapsed < self.interval:
            time.sleep(self.interval - elapsed)
        self._last_call = time.monotonic()


_rate_limiter = _RateLimiter(max_per_minute=20)


# ---------------------------------------------------------------------------
# Вызов GigaChat
# ---------------------------------------------------------------------------

def _call_gigachat(user_message: str, auth_key: str) -> str:
    _rate_limiter.wait()

    token = _token_manager.get_token(auth_key)

    resp = httpx.post(
        GIGACHAT_API_URL,
        headers={
            "Authorization": f"Bearer {token}",
            "Content-Type": "application/json",
        },
        json={
            "model": GIGACHAT_MODEL,
            "max_tokens": 256,
            "messages": [
                {"role": "system", "content": SYSTEM_PROMPT},
                {"role": "user",   "content": user_message},
            ],
        },
        verify=False,
        timeout=20,
    )
    resp.raise_for_status()
    return resp.json()["choices"][0]["message"]["content"]


