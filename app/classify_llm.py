    if _fast_deny(s):
        return Classified(False, -10, "MARKET_CASES", reason="fast_deny", summary="")

    user_msg = _build_user_message(source_id, title, text)

    raw = None
    parsed = None
    for attempt in range(1, 3):  # максимум 2 попытки
        try:
            raw = _call_gigachat(user_msg, auth_key)
        except httpx.HTTPStatusError as e:
            print(f"[LLM HTTP ERROR] {e.response.status_code} | {source_id} | {title[:60]}")
            print(f"[LLM HTTP BODY] {e.response.text[:300]}")
            return _FALLBACK
        except Exception as e:
            print(f"[LLM ERROR] {source_id} | {title[:60]} | {e}")
            return _FALLBACK

        parsed = _parse_response(raw)
        if parsed:
            break
        print(f"[LLM PARSE ERROR attempt={attempt}] {source_id} | {title[:60]} | raw={raw[:120]}")
        if attempt < 2:
            time.sleep(2)  # небольшая пауза перед retry

    if not parsed:
        return _FALLBACK

    keep       = bool(parsed.get("keep", False))
    _VALID_TYPES = {"LAW_DRAFT", "LAW_ADOPTED", "GUIDANCE", "ENFORCEMENT", "COURTS", "MARKET_CASES"}
    event_type = str(parsed.get("event_type", "MARKET_CASES"))
    if event_type not in _VALID_TYPES:
        event_type = "MARKET_CASES"
    tags       = [str(t) for t in parsed.get("tags", []) if isinstance(t, str)]
    reason     = str(parsed.get("reason", ""))
    summary    = str(parsed.get("summary", "")) if keep else ""
    score      = 10 if keep else -10

    return Classified(keep=keep, score=score, event_type=event_type, tags=tags, reason=reason, summary=summary)