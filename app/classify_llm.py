FAST_DENY = [
    # Война и СВО
    r"\bвсу\b|\bсво\b",
    r"\bфронт\b|\bнаступлени\b|\bвоенн\w*\s+операц\b",
    r"\bтцк\b|\bмобилизован\w*\b",                          # украинская мобилизация
    r"\bармия\s+рф\b|\bосвободил\w*\s+.{0,20}\b(днр|лнр|запорожск|херсонск)\b",
    # Ближний Восток — GigaChat отказывается обрабатывать
    r"\bиран\b|\bизраил\b|\bксир\b",
    r"\bкувейт\b|\bбахрейн\b|\bирак\b|\bйемен\b|\bхути\b",
    r"\bнато\b|\bпентагон\b|\bгосдеп\b",
    r"\bвоенн\w*\s+корабл\w*\b|\bавианосц\w*\b|\bвертолетн\w*\s+баз\w*\b",
    # Уголовка — физлица
    r"\bприговор(ил|ен)?\b|\bколони(я|и)\b|\bлишени(е|я)\s+свобод\w*\b",
    r"\bвозбуд(ил|или)\s+уголовн\w*\s+дело\b",
    r"\bтерроризм\b|\bтеракт\b",
    r"\bубийств\b|\bизнасил\b",
    # Быт и образование
    r"\bегэ\b|\bшкол\w*\b|\bгимназ\w*\b",
    r"\bмногодетн\w*\b|\bалименты\b|\bматеринск\w*\s+капитал\b",
    # Поздравления и события
    r"\bпоздравил\w*\b|\bпоздравлени\w*\b|\b8\s+март\w*\b",
    r"\bюбиле\w*\b|\bгодовщин\w*\b|\bцеремони\w*\b",
    # Спорт и развлечения
    r"\bфутбол\b|\bхоккей\b|\bтеннис\b|\bчемпионат\b",
    r"\bконцерт\b|\bмюзикл\b|\bспектакл\b|\bгороскоп\b",
]


def _fast_deny(text: str) -> bool:
    for pat in FAST_DENY:
        if re.search(pat, text, re.I):
            return True
    return False


