# Парсинг ответа
# ---------------------------------------------------------------------------

def _parse_response(raw: str) -> Optional[dict]:
    raw = raw.strip()
    raw = re.sub(r"^```(?:json)?\s*", "", raw)
    raw = re.sub(r"\s*```$", "", raw)

    # Попытка 1: прямой парсинг
    try:
        return json.loads(raw)
    except Exception:
        pass

    # Попытка 2: вырезать первый JSON-объект целиком
    m = re.search(r"\{.*\}", raw, re.DOTALL)
    if m:
        try:
            return json.loads(m.group())
        except Exception:
            pass

    # Попытка 3: GigaChat иногда ломает reason кириллическими кавычками или переносами.
    # Вырезаем reason и tags — они необязательны, keep/event_type важнее.
    stripped = re.sub(r',\s*"reason"\s*:\s*"[^"]*(?:"[^"]*)*"', '', raw)
    stripped = re.sub(r',\s*"tags"\s*:\s*\[[^\]]*\]', '', stripped)
    m2 = re.search(r"\{.*\}", stripped, re.DOTALL)
    if m2:
        try:
            return json.loads(m2.group())
        except Exception:
            pass

    # Попытка 4: вытащить keep и event_type напрямую регексами
    keep_m = re.search(r'"keep"\s*:\s*(true|false)', raw, re.I)
    event_m = re.search(r'"event_type"\s*:\s*"([A-Z_]+)"', raw)
    if keep_m:
        return {
            "keep": keep_m.group(1).lower() == "true",
            "event_type": event_m.group(1) if event_m else "MARKET_CASES",
            "tags": [],
            "reason": "parse_fallback",
        }

    return None


