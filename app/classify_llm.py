GIGACHAT_MODEL    = "GigaChat"

_TOKEN_CACHE_FILE = Path(os.environ.get("GIGACHAT_TOKEN_CACHE", "/tmp/gigachat_token.json"))


class _TokenManager:
    def get_token(self, auth_key: str) -> str:
        cached = self._load_cache()
        if cached:
            return cached

        resp = httpx.post(
            GIGACHAT_AUTH_URL,
            headers={
                "Content-Type": "application/x-www-form-urlencoded",
                "Accept": "application/json",
                "RqUID": "0deeb919-bfd8-45ed-a502-13eba6f85498",
                "Authorization": f"Basic {auth_key}",
            },
            data={"scope": "GIGACHAT_API_PERS"},
            verify=False,
            timeout=15,
        )
        resp.raise_for_status()
        data = resp.json()

        token = data["access_token"]
        expires_at = data.get("expires_at", 0) / 1000

        self._save_cache(token, expires_at)
        return token

    def _load_cache(self) -> Optional[str]:
        try:
            if not _TOKEN_CACHE_FILE.exists():
                return None
            cached = json.loads(_TOKEN_CACHE_FILE.read_text())
            if cached.get("expires_at", 0) > time.time() + 60:
                return cached["token"]
        except Exception:
            pass
        return None

    def _save_cache(self, token: str, expires_at: float) -> None:
        try:
            _TOKEN_CACHE_FILE.write_text(
                json.dumps({"token": token, "expires_at": expires_at})
            )
        except Exception:
            pass


_token_manager = _TokenManager()


class _RateLimiter:
    def __init__(self, max_per_minute: int = 20):
        self.interval = 60.0 / max_per_minute
        self._last_call: float = 0.0

    def wait(self) -> None:
        now = time.monotonic()
        elapsed = now - self._last_call
        if elapsed < self.interval:
            time.sleep(self.interval - elapsed)
        self._last_call = time.monotonic()


_rate_limiter = _RateLimiter(max_per_minute=20)


def _call_gigachat(user_message: str, auth_key: str) -> str:
    _rate_limiter.wait()

    token = _token_manager.get_token(auth_key)

    resp = httpx.post(
        GIGACHAT_API_URL,
        headers={
            "Authorization": f"Bearer {token}",
            "Content-Type": "application/json",
        },
        json={
            "model": GIGACHAT_MODEL,
            "max_tokens": 256,
            "messages": [
                {"role": "system", "content": SYSTEM_PROMPT},
                {"role": "user",   "content": user_message},
            ],
        },
        verify=False,
        timeout=20,
    )
    resp.raise_for_status()
    return resp.json()["choices"][0]["message"]["content"]


def _parse_response(raw: str) -> Optional[dict]:
    raw = raw.strip()
    raw = re.sub(r"^```(?:json)?\s*", "", raw)
    raw = re.sub(r"\s*```$", "", raw)

    try:
        return json.loads(raw)
    except Exception:
        pass

    m = re.search(r"\{.*\}", raw, re.DOTALL)
    if m:
        try:
            return json.loads(m.group())
        except Exception:
            pass

    stripped = re.sub(r',\s*"reason"\s*:\s*"[^"]*(?:"[^"]*)*"', '', raw)
    stripped = re.sub(r',\s*"tags"\s*:\s*\[[^\]]*\]', '', stripped)
    m2 = re.search(r"\{.*\}", stripped, re.DOTALL)
    if m2:
        try:
            return json.loads(m2.group())
        except Exception:
            pass

    keep_m = re.search(r'"keep"\s*:\s*(true|false)', raw, re.I)
    event_m = re.search(r'"event_type"\s*:\s*"([A-Z_]+)"', raw)
    summary_m = re.search(r'"summary"\s*:\s*"((?:[^"\\]|\\.)*)"', raw, re.DOTALL)

    if keep_m:
        summary = ""
        if summary_m:
            try:
                summary = json.loads(f'"{summary_m.group(1)}"')
            except Exception:
                summary = summary_m.group(1)

        return {
            "keep": keep_m.group(1).lower() == "true",
            "event_type": event_m.group(1) if event_m else "MARKET_CASES",
            "tags": [],
            "reason": "parse_fallback",
            "summary": summary,
        }

    return None


