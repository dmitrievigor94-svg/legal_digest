import re

TOPIC_OTHER = "прочее"
TOPIC_LAW = "законодательство"
TOPIC_REG = "регуляторы"
TOPIC_FINES = "штрафы"
TOPIC_COURTS = "судебная практика"
TOPIC_PDN = "персональные данные"
TOPIC_IP = "интеллектуальная собственность"
TOPIC_CONSUMERS = "потребители/реклама"

def detect_topic(title: str, text: str, source_id: str) -> str:
    t = (title or "").lower()
    body = (text or "").lower()
    s = f"{t} {body}"

    # Судебка
    if re.search(r"\b(верховн(ый|ого)\s+суд|суд|арбитраж|апелляц|кассац|определени|постановлени|дело\s*№)\b", s):
        return TOPIC_COURTS

    # Персональные данные
    if re.search(r"\b(персональн(ые|ых)\s+данн|152(?:-| )?фз|роскомнадзор|утечк|согласие\s+на\s+обработк)\b", s):
        return TOPIC_PDN

    # IP
    if re.search(r"\b(товарн(ый|ого)\s+знак|патент|авторск(ое|их)\s+прав|лицензионн|контрафакт|роcпатент|бренд)\b", s):
        return TOPIC_IP

    # Потребители/реклама
    if re.search(r"\b(реклама|маркетинг|потребител|оферт|подписк|возврат|навязанн|кешбэк|акци(я|и))\b", s):
        return TOPIC_CONSUMERS

    # Штрафы/привлечение/дела/предупреждения (ФАС)
    if re.search(r"\b(предупрежден|возбудил(а|о)\s+дело|дело\s+в\s+отношен|привлечен|нарушени|административн)\b", s):
        return TOPIC_FINES

    # Регуляторика
    if re.search(r"\b(рекомендац|разъяснен|информационн(ое|ого)\s+письм|письмо|приказ|методик|утвержден|фас|банк\s+россии|центральн(ый|ого)\s+банк)\b", s):
        return TOPIC_REG

    # Законодательство
    if re.search(r"\b(законопроект|внес(ен|ено)|госдум|совет\s+федерац|принят(ие|)\s+закона|вступа(ет|ют)\s+в\s+силу|федеральн(ый|ого)\s+закон|проект\s+фз)\b", s):
        return TOPIC_LAW

    # Источники ФАС обычно регуляторные
    if source_id.startswith("fas_"):
        return TOPIC_REG

    return TOPIC_OTHER