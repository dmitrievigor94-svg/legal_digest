def _cbr_is_relevant(title: str, url: str) -> bool:
    t = title.lower()
    u = (url or "").lower()

    # URL-whitelist (НПА и пресс-релизы)
    for hint in ("/crosscut/lawacts/file/", "/queries/unidbquery/file/",
                 "/project_na/", "/press/pr/"):
        if hint in u:
            return True

    # Сначала режем явный макро-мусор
    if _match_any(_CBR_DENY, t):
        return False

    # Затем проверяем наличие регуляторного хинта
    return _match_any(_CBR_ALLOW, t)


