# Источники, которые полностью скипаем (дублируют SKIP_SOURCES в classify_llm.py)
_SKIP_SOURCES = {"gov_all", "fts_news"}


def is_relevant(source_id: str, title: str, url: str) -> bool:
    """
    Возвращает False если новость точно не нужна — дешёвый regex-фильтр.
    Возвращает True если нужно отправить на LLM для финального решения.
    """
    t = (title or "").strip()
    u = (url or "").strip()

    # Полностью скипаемые источники
    if source_id in _SKIP_SOURCES:
        return False

    # Глобальный технический мусор
    if _match_any(_GLOBAL_HARD_DENY, t.lower()):
        return False

    # Военная тема — GigaChat откажется отвечать
    if _match_any(_WAR_DENY, t.lower()):
        return False

    if source_id in ("cbr_events", "cbr_press"):
        return _cbr_is_relevant(t, u)

    if source_id.startswith("drussia_"):
        return _drussia_is_relevant(t)

    if source_id.startswith("consultant_"):
        return _consultant_is_relevant(source_id, t, u)

    if source_id == "rg_main":
        return _rg_is_relevant(t)

    if source_id == "rpn_news":
        return _rpn_is_relevant(t)

    # Все остальные источники (fas, rapsi, pravo_ru, cbr_press, eg_online) —
    # не режем, пускаем на LLM. Они в целом релевантны.
    return True